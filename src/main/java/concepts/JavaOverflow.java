package concepts;

public class JavaOverflow {
    public static void main(String[] args) {
        int x = Integer.MAX_VALUE;
        System.out.println(x);
        x = x + 1;
        System.out.println(x);
        x= x +2;
        System.out.println(x);

        int y = Integer.MIN_VALUE;
        System.out.println(y);
        y = y-1;
        System.out.println(y);
        y=y-2;
        System.out.println(y);

    }
}
